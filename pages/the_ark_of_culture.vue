<pre class="annotation_en" style="text-align: left;">On the 25th of May 2019 the place known in Krasnodar as the "cemetery of barges" was occupied and within its framework the exhibition of 22 artists and a concert of synth-pop musician VATSKYY was held. The event was named "The Ark of Culture" and the Independent Branch of the Kuban River Shipping Company was the mythological organizer (as an opposition to the organization that owns this territory).

« ... you will be able to walk a winding path to the Ark and escape from the oppression of asphalt, fences and cars. Or plunge into an even bigger industrial hell ... »

Barges are located between two coasts occupied by different forces — on the one hand it is the Kuban River Shipping Company, a business territory (they extract sand here and also own other coastal areas where elite housing is being built), which has taken a part of the coastline and does not give access to it; on the other hand it is a state institution "Regional Center for Sports Training #1", where we have always seen only a guard. This center has grasped a huge peninsula, having separated the territory by grid Rabitsa (chain-link fence), having left a narrow strip of coast to inhabitants of the city. In addition, the passage to the peninsula is also fenced with a corrugated galvanised iron, but fortunately a small passage to the peninsula was left. Having passed two layers of fences, it was necessary to go on a narrow coastal path along a wood along a fence through mosquitoes and gnats on a dirt. At the end of this journey, having overcome the wooden gangway we had assembled, the visitors came aboard rusty, apparently very seldom used, ships that were connected by ropes. Invited artists could use the inside bilges and outside elements of barges. The concert was organized inside one of the barges, and the lighting and equipment were powered by a small generator located on the adjacent barge.

Located between the two occupied shores on a rusty vessel that was still difficult to reach, the barges were a direct illustration of the <a href="https://instructions.stranno.su/en/#legal">semi-legality</a> — an emerging void in the midst of the competition between the authorities and the lack of carrying power that we tried to fill. We would like to thank the brave artists and visitors who, despite the rain, came to participate in this filling.

